## while ( em quanto)

i=1
 while i <= 50
    puts "JUNIOR CONDE - #{i}" #{1} serve para mostrar indicadores
    i += 1
 end

# QDO CHEGAR NO 50, VAMOS FAZER RETORNAR
# i == 50un

until i <= 0 # enquanto 50 for maior que zero ele vai fazer essa repetição 
   puts "Sou Maior que Zero -#{i}"
   i -= 1
end

